/**
 * Main employee class with basic properties.
 */
export class Employee {
  /**
   * Initializes an instance with a name and a surname.
   * @param {String} name - First name of employee.
   * @param {String} surname - Last name of employee.
   */
  constructor(name, surname) {
    this.name = name;
    this.surname = surname;
  }
}
